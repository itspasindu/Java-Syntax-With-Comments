public class Varibales {
    public static void main(String[] args) {
        // x = 10; initialize the variable x with a value of 10
        int x; // Declare a variable x
        x = 10; // Assign a value to x

        double y = 3.14; // Declare and initialize a variable y with a value of 3.14
        boolean z = true; // Declare and initialize a variable z with a value of true

        System.out.println(x); // Display the value of x. don't use "" for the variable.

        System.out.println("My Number Is: "+ x + "!"); // Display the value of x with a string. use "" for the string.
        System.out.println("Precentage Is: " + y);
        System.out.println("Boolean Value: " + z);
    }
}

